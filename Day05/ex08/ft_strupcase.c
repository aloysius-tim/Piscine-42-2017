char	*ft_strupcase(char *str)
{
	int counter;

	counter = 0;
	while (*str != '\0')
	{
		if (((*str) >= 'a') && ((*str) <= 'z'))
		{
			(*str) = (*str) - 32;
		}
		str++;
		counter++;
	}
	return (str - counter);
}
