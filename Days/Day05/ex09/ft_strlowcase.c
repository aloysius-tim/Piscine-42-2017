char	*ft_strlowcase(char *str)
{
	int counter;

	counter = 0;
	while (*str != '\0')
	{
		if (*str >= 'A' && *str <= 'Z')
		{
			(*str) = (*str) + ('a' - 'A');
		}
		str++;
		counter++;
	}
	return (str - counter);
}
