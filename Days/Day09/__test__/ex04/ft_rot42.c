char	*ft_rot42(char *str)
{
	char *head;

	head = str;
	while (*str != '\0')
	{
		if (*str >= 'a' && *str <= 'z')
			(*str) = (*str - 'a' + 16) % 26 + 'a';
		else if (*str >= 'A' && *str <= 'Z')
			(*str) = (*str - 'A' + 16) % 26 + 'A';
		str++;
	}
	return (head);
}
